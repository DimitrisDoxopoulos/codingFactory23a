package gr.aueb.cf.ch2;

/**
 * Intro to Math Class
 */
public class MathApp {
    public static void main(String[] args) {

        System.out.println("The absolute value of -5 is: " + Math.abs(-5));
        System.out.println("The minimum value between -5  and 5 is: " + Math.min(-5, 5));
        System.out.println("The maximum value between -5  and 5 is: " + Math.max(-5, 5));
    }
}
